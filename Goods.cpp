void Item::showItem(){
	cout << name << endl;
	cout << desc << endl;
	if (id >= 0 && id <= 7) { //武器
		cout << "攻击力：" << getaddAttack() << endl;
		cout << "防御力：" << getaddDefend() << endl;
	}
	else if (id <= 15) { // 药品
		cout << "增加气血：" << getaddHP() << endl;
		cout << "增加内力：" << getaddMP() << endl;
	}
	else { // 防具
		cout << "攻击力：" << getaddAttack() << endl;
		cout << "防御力：" << getaddDefend() << endl;
	}
}

